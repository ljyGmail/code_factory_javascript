/**
 * Variable 선언하기
 *
 * 1) var - 더이상 쓰지 않는다.
 * 2) let
 * 3) const
 */

var name = "코드팩토리";
console.log(name);

var age = 32;
console.log(age);

let sukumaSwitch = "스키마스위치";
console.log(sukumaSwitch);

/**
 * let과 var로 선언하면
 * 값을 추후 변경할 수 있다.
 */
sukumaSwitch = 'Line';
console.log(sukumaSwitch);

const view = 'View';
console.log(view);

